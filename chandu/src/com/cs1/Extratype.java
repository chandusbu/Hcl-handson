package com.cs1;

public class Extratype {


String name;
String runs;

public Extratype(String name, String runs) {
	super();
	this.name = name;
	this.runs = runs;
}

	




	public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getRuns() {
	return runs;
}
public void setRuns(String runs) {
	this.runs = runs;
}}




